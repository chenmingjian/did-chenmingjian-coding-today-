# @lc code=start
class Solution:
    def searchMatrix(self, matrix, target):
        """
        :type matrix: List[List[int]]
        :type target: int
        :rtype: bool
        """
        a = []
        for i in matrix:
            a += i

        return target in a

# @lc code=end

